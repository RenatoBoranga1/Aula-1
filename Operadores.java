public class Operadores {   
    public static void main(String[] args) {
        
        int a = 10 ;
        int b = 2;
        int c = a + b;


        c = a + b;
        System.out.println(c);

        c = a - b;
        System.out.println(c);

        c = a / b;
        System.out.println(c);

        c = a * b;
        System.out.println(c);

    }
    
}
